package com.demo.demo.keyword;

public class BinaryIntegralLiteralDemo {
    public static void main(String[] args) {
        System.out.println(0b010101);
        System.out.println(0B010101);
        System.out.println(0x15A);
        System.out.println(0X15A);
        System.out.println(077);
        System.out.println(5_000);
        /**
         * 输出结果
         * 21
         * 21
         * 346
         * 346
         * 63
         * 5000
         */
    }
}
